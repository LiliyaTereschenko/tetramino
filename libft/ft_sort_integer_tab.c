void	ft_sort_integer_tab(int *tab, int size)
{
	int i;
	int a;

	i = 0;
	a = 0;
	while (i <= size)
	{
		if (tab[i] <= tab[i + 1])
		{
			i++;
		}
		else
		{
			a = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = a;
			i = 0;
		}
	}
}
