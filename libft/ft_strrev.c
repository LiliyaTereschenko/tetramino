#include "libft.h"

char	*ft_strrev(char *str)
{
	char	i;
	int		k;
	int		q;

	q = 0;
	k = ft_strlen(str);
	while (q <= k / 2)
	{
		i = str[q];
		str[q] = str[k - q - 1];
		str[k - q - 1] = i;
		q++;
	}
	return (str);
}
