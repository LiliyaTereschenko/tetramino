#include "libft.h"

void	ft_bzero(void *dest, size_t n)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)dest;
	while (n > 0)
	{
		*ptr = 0;
		ptr++;
		n--;
	}
}
